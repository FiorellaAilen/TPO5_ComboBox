    private void cargarProducto(Producto p){
        table.addRow(new Object[]{p.getNombre(), p.getCategoria(), p.getPrecio()});
    }
    
    private void armarTable(){
        table.addColumn("nombre");
        table.addColumn("categoria");
        table.addColumn("precio");
        jttabla.setModel(table);
    }


